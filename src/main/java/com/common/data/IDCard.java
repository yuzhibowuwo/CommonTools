    public char CaluCrc(String IdNo)
    {
        int res=0;
        if (IdNo.length()!=17)
            return 'X';
        for (int i=0;i<IdNo.length();i++) {

            res = res + Weight.get(i+1) *  ((int)IdNo.charAt(i)-48);
        }
        return CrCode[res%11];

    }
